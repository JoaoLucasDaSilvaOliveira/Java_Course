package Class_Content;

public class ExplicitCasting {
    public static void main(String[] args) {
        long n = 1000000000000000000L;
        int n1 = (int)n;
        System.out.println(n);
        System.out.println(n1);
        long n2 = 10l;
        int n3 = (int)n2;
        System.out.println(n2);
        System.out.println(n3);
    }
}
